package day08_relational_operators;

public class SighType {
    public static void main(String[] args) {

        int num1 = 2;


        boolean isTheNumberPositive = num1 > 0;
        boolean isTheNumberNegative = num1 < 0;
        boolean isTheNumber0 = num1 == 0;

        System.out.println(num1 + " is positive " + isTheNumberPositive);
        System.out.println(num1 + " is negative " + isTheNumberNegative);
        System.out.println(num1 + " is zero " + isTheNumber0);





    }

}

package day33_methods;

public class GetCharacters {



    public static void letterAtoZUpper(){

        for (char i = 'A'; i <= 'Z'; i++){

            System.out.print(i + " ");
    }
        System.out.println();
    }


    public static void letterZtoAUpper(){

        for (char i = 'Z'; i >= 'A'; i--){

            System.out.print(i + " ");
        }
        System.out.println();
    }

    public static void letterAtoZLower(){

        for (char i = 'a'; i <= 'z'; i++){

            System.out.print(i + " ");
        }
        System.out.println();
    }


    public static void letterZtoALowers(){

        for (char i = 'z'; i >= 'a'; i--){

            System.out.println(i + " ");
        }
        System.out.println();
    }




    public static void numbers(){

        for (char i = '1'; i <= '9'; i++){

            System.out.print(i + " ");
        }
        System.out.println();
    }
















}

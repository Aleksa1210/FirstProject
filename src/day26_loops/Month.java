package day26_loops;

public class Month {
    public static void main(String[] args) {



        for (int week = 1; week <= 4; week++ ){
            System.out.println("week: " + week);
            for (int days = 1; days <= 7; days++){
                System.out.println("\tdays: " + days);
        }
        }











    }
}
